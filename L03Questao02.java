package lista03;

public class L03Questao02 {

	public static void main(String[] args) {
		for (int i = 0; i <= 9; i++) {
			System.out.println("\n Tabuada do " + i);

			for (int numero = 1; numero <= 10; numero++) {
				System.out.println(i + " x " + numero + " = " + i * numero);
			}

		}

	}

}